public class Person {

    public String name;
    public String work;
    public int age;
    public String sex;
    public int length;
    public String house;

    public Person(String n, String w, int a, String s, int l, String h) {
        name = n;
        work = w;
        age = a;
        sex = s;
        length = l;
        house = h;

    }


}
